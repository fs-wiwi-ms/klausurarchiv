import Ecto.Query

alias Klausurarchiv.Repo
alias Klausurarchiv.Uploads
alias Klausurarchiv.Uploads.{Exam, Lecture, Term}

Enum.map(exams, fn %{
                     file_name: file,
                     year: year,
                     term: term,
                     exam_name: exam_name
                   } ->
  term = Repo.get_by(Term, year: year, type: term)

  lecture =
    case Repo.get_by(Lecture, name: exam_name) do
      nil ->
        %Lecture{}
        |> Lecture.changeset(%{"name" => exam_name, "degrees" => []})
        |> Repo.insert!()

      %Lecture{} = lecture ->
        lecture
    end

  exam =
    case Repo.get_by(Exam, filename: file) do
      nil ->
        %Exam{}
        |> Exam.changeset(%{
          "filename" => file,
          "lecture_id" => lecture.id,
          "term_id" => term.id,
          "published" => true
        })
        |> Repo.insert!()

      %Exam{} = exam ->
        exam
    end
end)
